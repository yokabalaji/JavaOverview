package Basics;

public class SwichStr {
	public static void main(String[] args) {
		String a="madhurai";
		switch(a) {
		case "madhurai":
			System.out.println("sunday");
			break;
		case "chennai":
			System.out.println("sunday");
			break;
	
			default :
				System.out.println("invalid num");
				break;
		}
		
	}
	}
